# Assining different variables
name = "Aarav"
age = 10
weight = 29.5
is_student = True

# Printing different variables and their datatypes
print("Name:", name)
print("The Datatype for name is:", type(name))

print("Age:", age)
print("The Datatype for age is:", type(age))

print("Weight:", weight)
print("The Datatype for weight is:", type(weight))

print("Is student:", is_student)
print("The Datatype for is student is:", type(is_student))

# Converting variables to different Datatypes
age = str(age)
print(age)
print("The Datatype for age is:", type(age))
weight = bool(weight)
print(weight)
print("The Datatype for weight is:", type(weight))